# int
a = 10
print(type(a))

# float
a = 7.2
print(type(a))

# complex
a = 7+5j
print(type(a))

# list
a = [1, 2, 3, 4, 5]
print(type(a))

# tuple
a = (1, 2, 3, 4, 5)
print(type(a))

# set
a = {1, 2, 3, 4, 5}
print(type(a))

# string
a = 'Naitik Yadav'
print(type(a))

# boolean
a = True
print(type(a))
